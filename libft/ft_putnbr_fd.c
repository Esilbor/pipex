#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	nbr;
	char		temp;

	nbr = n;
	if (nbr < 0)
	{
		nbr *= -1;
		ft_putchar_fd('-', fd);
	}
	if (nbr > 9)
		ft_putnbr_fd(nbr / 10, fd);
	temp = nbr % 10 + '0';
	ft_putchar_fd(temp, fd);
}
